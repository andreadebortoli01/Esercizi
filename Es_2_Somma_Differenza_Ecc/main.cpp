#include <iostream>

using namespace std;

int main()
{
    float x = 0;
    float y = 0;

    cout <<"Inserire prima cifra  ";
    cin >> x;
    cout <<"Inserire seconda cifra  ";
    cin >> y;

    cout << "Somma="; cout << x+y; cout << "\n" ;
    cout << "Differenza="; cout << x-y; cout << "\n" ;
    cout << "Moltiplicazione="; cout << x*y; cout << "\n" ;
    cout << "Divisione="; cout << x/y; cout << "\n" ;
}
